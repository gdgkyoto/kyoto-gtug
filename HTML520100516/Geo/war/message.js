function getMessage(distance){
	if(distance < 5){
		return "5km";
	}else if(distance < 10){
		return "10km";
	}else if(distance < 15){
		return "15km";
	}else if(distance < 20){
		return "20km";
	}else if(distance < 25){
		return "25km";
	}else if(distance < 30){
		return "30km";
	}else if(distance < 35){
		return "35km";
	}
}